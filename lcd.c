void Init_SPI(void)
{

P3OUT&=~BIT7;
  P3DIR    &=~ BIT7                                       ;

  P3SEL    |= BIT7                                        ; //功能选择为SPI

  P5OUT&=~BIT5;
  P5DIR&=~BIT5;

  P5SEL|=BIT5;



  UCB1CTL1 |= UCSWRST                                     ; // 复位SPI状态机
  UCB1CTL0 |= UCMST+UCSYNC+UCCKPL+UCMSB                   ; // 3-pin, 8-bit SPI master, Clock polarity high, MSB
  UCB1CTL1 |= UCSSEL_2                                    ; // 选择SCK参考源为SMCLK
  UCB1BR0   = 0x02                                        ; // SCK = SMCK/2
  UCB1BR1   = 0                                           ;
  UCB1CTL1 &=~UCSWRST                                     ; // SPI状态机使能
}

void Init_Port(void)
{
  P10DIR  |= BIT0+BIT1+BIT2+BIT3                                       ; // 主电源


}




void delaylcd(unsigned int n)                 //1ms延时函数
  {
   unsigned int i;
   for (i=0;i<n;i++)
	   delaylcd_us(1000);
  }
void delaylcd_us(unsigned int n)
{

	unsigned int i;
	  for (i=0;i<n;i++)
		  _delay_cycles(24);
}
void Init_LCD()
{

    LCD_PWR_0;
    delay(1);
	LCD_RST_0;
	delay(1);
	LCD_RST_1;

	LCD_CE_1;
	delay_us(1);
	LCD_CE_0;

	delay(1);


    LCD_Write_Byte(0x21, 0);	// 使用扩展命令设置LCD模式
    LCD_Write_Byte(0xc8, 0);	// 设置偏置电压
    LCD_Write_Byte(0x06, 0);	// 温度校正
    LCD_Write_Byte(0x13, 0);	// 1:48
    LCD_Write_Byte(0x20, 0);	// 使用基本命令
    LCD_Clear();	        // 清屏
    LCD_Write_Byte(0x0c, 0);	// 设定显示模式，正常显示

    LCD_CE_1;
}

void LCD_Write_Byte(unsigned char data,unsigned char dc)
{
	LCD_CE_0;
	if(dc==0)
		LCD_DC_0;
	else
		LCD_DC_1;
	delay_us(5);
	while (!(UCB1IFG&UCTXIFG));
	UCB1TXBUF=data;

	delay_us(5);

	LCD_CE_1;
}
void LCD_Clear(void)
  {
    unsigned int i;
    LCD_Write_Byte(0x0c, 0);
    LCD_Write_Byte(0x80, 0);
    for (i=0; i<504; i++)
      LCD_Write_Byte(0, 1);
  }
void LCD_Set(unsigned char x,unsigned char y)
{
	LCD_Write_Byte(0x40 | y, 0);		// column
	LCD_Write_Byte(0x80 | x, 0);          	// row
}

void LCD_Char(unsigned char c)
{
	unsigned char line;
	    c -= 32;
	    for (line=0; line<6; line++)
	      LCD_Write_Byte(font6x8[c][line], 1);
}

void LCD_Printf(unsigned char x,unsigned char y,char *s)
  {
    LCD_Set(x,y);
    while (*s)
      {
	 LCD_Char(*s);
	 s++;
      }
  }
